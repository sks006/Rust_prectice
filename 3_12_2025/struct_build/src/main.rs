enum Status{
    Pass,
    reje
}
struct Loan{
    acc:u32,
    status:Status
}

impl Loan{
    fn new(a:u32,s:Status)->Self{
        Self{
            acc:a,
            status:s
        }
    }
}

fn main(){
    let l= Loan::new(33,Status::Pass);
    match l.status{
        Status::Pass=>println!("pass {}",l.acc),
        Status::reje=>println!("reje {}",l.acc),
    }
}
