fn find_index(arr:&[i32],target:i32)->Option<usize>{
    for(index,&value)in arr.iter().enumerate(){
        if value==target{
            return Some(index)
        }
    }
    None
}
fn main(){
    let arr=[1,3,5,7,9];
    let target=5;
    match find_index(&arr,target){
        Some(i)=>println!("Found at index: {}",i),
        None=>println!("Not found"),
    }
}